"""
数学基础 #1：向量与矩阵（AI 的"积木"）
=========================================
你在 attention.py 里看到的 X、Q、K、V 都是「矩阵」，
@ 是「矩阵乘法」，.T 是「转置」。
这节课用最简单的例子把它们讲清楚。看懂数字怎么变，比背公式管用 10 倍。

【比喻】
- 向量：一排数字，像一个人的各项指标 [身高, 体重, 年龄]
- 矩阵：一张数字表，像一群人的指标表（每行一个人）
"""

import numpy as np

np.set_printoptions(precision=2, suppress=True)

# =================================================================
# 【1】向量 vector：一排数字
# =================================================================
print("=" * 55)
print("【1】向量 vector —— 一排数字")
print("=" * 55)
person = np.array([175, 70, 30])   # 一个人：身高175 体重70 年龄30
print("一个人:", person)
print("形状 .shape =", person.shape, " -> (3,) = 3 个数字排成一排")
print("（形状就是告诉你：这个东西由几个数字、怎么排列组成）\n")

# =================================================================
# 【2】矩阵 matrix：一张数字表（多行向量叠起来）
# =================================================================
print("=" * 55)
print("【2】矩阵 matrix —— 一张数字表")
print("=" * 55)
people = np.array([
    [175, 70, 30],   # 张三
    [160, 55, 25],   # 李四
    [180, 80, 35],   # 王五
])
print("一群人（3 人 x 3 指标）:\n", people)
print("形状 =", people.shape, " -> (行数=3, 列数=3)")
print("约定：行 = 样本（人），列 = 特征（指标）\n")

# =================================================================
# 【3】转置 .T：行列互换（attention 里 K.T 就是它）
# =================================================================
print("=" * 55)
print("【3】转置 .T —— 行列互换")
print("=" * 55)
print("原来的（3人x3指标）:\n", people)
print("转置后 .T（变成 3指标x3人）:\n", people.T)
print("第 1 行 [175,160,180] 原来是三个人的身高——转置后单独成行\n")

# =================================================================
# 【4】矩阵乘法 @（attention 的核心运算！）
# =================================================================
print("=" * 55)
print("【4】矩阵乘法 @ —— 结果[i][j] = A 的第 i 行 点乘 B 的第 j 列")
print("=" * 55)
A = np.array([[1, 2],
              [3, 4]])
B = np.array([[5, 6],
              [7, 8]])
print("A:\n", A)
print("B:\n", B)
C = A @ B   # @ 就是矩阵乘法
print("A @ B =\n", C)
print()
print("手动验证：结果[0][0] 怎么来的？")
print("  = A 的第 0 行 [1,2] 和 B 的第 0 列 [5,7] 对应相乘再相加")
print("  = 1x5 + 2x7 =", 1*5 + 2*7, "   <- 和上面 C[0][0] 一样")
print("结果[0][1]:")
print("  = A 的第 0 行 [1,2] 和 B 的第 1 列 [6,8]")
print("  = 1x6 + 2x8 =", 1*6 + 2*8, "   <- 和上面 C[0][1] 一样\n")

# =================================================================
# 【5】点积 dot product：两个向量对应相乘再相加
# =================================================================
print("=" * 55)
print("【5】点积 dot —— 衡量两个向量有多像")
print("=" * 55)
v1 = np.array([1, 2, 3])
v2 = np.array([4, 5, 6])
print("v1:", v1)
print("v2:", v2)
print("点积 v1·v2 =", np.dot(v1, v2), " = 1x4 + 2x5 + 3x6")
print("关键直觉：点积越大 -> 两个向量越像、越相关")
print("  （数学上：方向越一致、长度越大，点积越大）\n")

# =================================================================
# 【6】连回 attention：Q @ K.T 到底在干嘛
# =================================================================
print("=" * 55)
print("【6】连回 attention.py：Q @ K.T 的真相")
print("=" * 55)
print("回忆 attention 第 2 步：scores = Q @ K.T")
print("  Q 形状 (词数, 维度)，K.T 形状 (维度, 词数)")
print("  相乘后 -> (词数, 词数) 的方表")
print()
print("这个表里每个数 = 一个词的向量 和 另一个词的向量 做点积")
print("  -> 也就是在问：词 i 和 词 j 有多像？")
print("  -> 越像 = 点积越大 = 关注度越高   <- 这就是 attention 的全部秘密！")
print()
print("=" * 55)
print("今天学会的积木：")
print("  向量(一排) -> 矩阵(一张表) -> 转置.T(行列互换)")
print("  -> 矩阵乘法@(行点乘列) -> 点积(相似度) -> Q@K.T(关注度表)")
print("=" * 55)
