"""
从零手写 Self-Attention（自注意力机制）
=========================================
目标：用几十行 numpy 代码，把面试必考的 self-attention 亲手跑一遍。
做完你就能回答：Q/K/V 是什么？softmax(QKᵀ/√d) 在算什么？为什么除以 √d？

【比喻】Self-Attention 就像一个"自我介绍大会"：
  - 每个词都要听别人说话，决定"我要多关注谁"
  - Q（Query，提问）：我想找什么样的信息
  - K（Key，标签）：我能提供什么 / 别人怎么找到我
  - V（Value，内容）：我实际携带的信息
  - 注意力分数 = 某个词的 Q 和所有词的 K 的匹配程度
  - 输出 = 用这些分数对所有词的 V 加权求和
"""

import numpy as np

np.set_printoptions(precision=3, suppress=True)

# =================================================================
# 第 0 步：准备输入——3 个词，每个词用一个 4 维向量表示（embedding）
# 这里用最简单的单位向量模拟"我 / 爱 / 学习"三个词
# （真实场景里，这些向量是 embedding 模型算出来的）
# =================================================================
X = np.array([
    [1.0, 0.0, 0.0, 0.0],   # "我"
    [0.0, 1.0, 0.0, 0.0],   # "爱"
    [0.0, 0.0, 1.0, 0.0],   # "学习"
])
print("=" * 60)
print("第 0 步：输入 X（3 个词，每个 4 维）")
print("=" * 60)
print(X)
print(f"形状: {X.shape}  →  (词数 seq_len=3, 向量维度 d=4)\n")

# =================================================================
# 第 1 步：创建 Q/K/V 三个权重矩阵（这是模型要"学习"的参数）
# 用 X 乘以它们，就把每个词的向量"投影"成 Q、K、V
# 这里用随机值演示，真实模型里这些矩阵是训练出来的
# =================================================================
d = 4  # 向量维度
np.random.seed(0)
W_Q = np.random.randn(d, d)   # 生成 Q 的权重矩阵
W_K = np.random.randn(d, d)   # 生成 K 的权重矩阵
W_V = np.random.randn(d, d)   # 生成 V 的权重矩阵

Q = X @ W_Q   # @ 是矩阵乘法：每个词的向量经过 W_Q 投影 → 得到 Q
K = X @ W_K
V = X @ W_V

print("=" * 60)
print("第 1 步：X 乘权重矩阵 → 得到 Q / K / V")
print("=" * 60)
print("Q（Query，每个词『想找什么』）：")
print(Q)
print("K（Key，每个词『提供什么标签』）：")
print(K)
print("V（Value，每个词『实际信息』）：")
print(V, "\n")

# =================================================================
# 第 2 步：算注意力分数——看每个词和其他词有多"匹配"
# 核心：Q @ K 的转置 → 得到一个 3×3 矩阵
# scores[i][j] 表示：第 i 个词对第 j 个词的关注程度（未归一化）
# =================================================================
scores = Q @ K.T   # K.T 是 K 的转置
print("=" * 60)
print("第 2 步：Q @ Kᵀ → 原始注意力分数（3×3）")
print("=" * 60)
print(scores)
print("解读：第 i 行第 j 列 = 词 i 对词 j 的关注度\n")

# =================================================================
# 第 3 步：除以 √d（缩放）—— ⚠️ 面试高频追问点！
# 为什么？Q·K 是两个向量的点积，维度越大，点积的数值越大。
# 数值太大 → 进 softmax 会变成『一个≈1、其余≈0』→ 梯度消失 → 训不动。
# 除以 √d 把方差拉回 1，让数值稳定。
# =================================================================
scaled_scores = scores / np.sqrt(d)
print("=" * 60)
print(f"第 3 步：除以 √d 缩放（d={d}, √d={np.sqrt(d):.3f}）")
print("=" * 60)
print(scaled_scores, "\n")

# =================================================================
# 第 4 步：softmax——把分数变成"概率"（每一行加起来 = 1）
# 这样关注程度就成了百分比，方便后面加权
# =================================================================
def softmax(x):
    # 减去最大值防止数值爆炸（这是工程上的数值稳定性小技巧）
    e = np.exp(x - np.max(x, axis=-1, keepdims=True))
    return e / np.sum(e, axis=-1, keepdims=True)

attn_weights = softmax(scaled_scores)
print("=" * 60)
print("第 4 步：softmax → 注意力权重（每行和 = 1，= 关注比例）")
print("=" * 60)
print(attn_weights)
print("解读：第 0 行 [a, b, c] 表示『我』分别对『我/爱/学习』的关注比例\n")

# =================================================================
# 第 5 步：用注意力权重对 V 加权求和 → 得到最终输出
# output[i] = 所有词的 V 按关注度加权平均
# 这就是"自注意力"的精髓：每个词的新表示融合了整句话的信息
# =================================================================
output = attn_weights @ V
print("=" * 60)
print("第 5 步：注意力权重 @ V → 最终输出")
print("=" * 60)
print(output)
print(f"输出形状: {output.shape}  →  和输入 X 一样（3×4）")

print("\n" + "=" * 60)
print("✅ 总结：self-attention 全流程")
print("=" * 60)
print("  X →(Wq,Wk,Wv)→ Q, K, V → Q@Kᵀ → /√d → softmax → ×V → 输出")
print("  核心：一个词的新表示 = 它对全句所有词的关注度 × 那些词的信息")
print("=" * 60)
